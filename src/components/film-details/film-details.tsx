import { Fragment } from 'react';
import { TFilmDetail } from '../../types/film';
import { formatFilmRunTime } from '../../utils';

type FilmDetailsProps = {
  film: TFilmDetail;
}

function FilmDetails({film}: FilmDetailsProps): JSX.Element {
  const {director, starring, runTime, genre, released} = film;

  return (
    <div className="film-card__text film-card__row">
      <div className="film-card__text-col">
        <p className="film-card__details-item">
          <strong className="film-card__details-name">Director</strong>
          <span className="film-card__details-value">{director}</span>
        </p>
        <p className="film-card__details-item">
          <strong className="film-card__details-name">Starring</strong>
          <span className="film-card__details-value">
            {starring.map((star, index) =>
              starring.length === ++index ?
                <Fragment key={star}>{star}</Fragment>
                :
                <Fragment key={star}>{`${star}, `}<br/></Fragment>
            )}
          </span>
        </p>
      </div>

      <div className="film-card__text-col">
        <p className="film-card__details-item">
          <strong className="film-card__details-name">Run Time</strong>
          <span className="film-card__details-value">{formatFilmRunTime(runTime)}</span>
        </p>
        <p className="film-card__details-item">
          <strong className="film-card__details-name">Genre</strong>
          <span className="film-card__details-value">{genre}</span>
        </p>
        <p className="film-card__details-item">
          <strong className="film-card__details-name">Released</strong>
          <span className="film-card__details-value">{released}</span>
        </p>
      </div>
    </div>
  );
}

export default FilmDetails;
